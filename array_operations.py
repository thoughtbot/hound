# Initialize an array
my_array = [1, 2, 3, 4, 5]

# Display the array elements
for i in range(len(my_array)):
    print(my_array[i], end=' ')

# Calculate the sum of array elements
array_sum = sum(my_array)
print("\nSum of array elements:", array_sum)

# Find the maximum element in the array
max_element = max(my_array)
print("Maximum element in the array:", max_element)

# Find the minimum element in the array
min_element = min(my_array)
print("Minimum element in the array:", min_element)
